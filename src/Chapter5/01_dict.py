# Dictionary in Python
# A dictionary is a collection of key-value pairs, where each key is unique and maps to a specific value.
# Dictionaries are unordered, changeable, and do not allow duplicate keys.
# Dictionaries are written with curly braces {} and use colons to separate keys and values.


d = {}  # Empty dictionary
marks = {"Harry": 100, "Shubham": 56, "Rohan": 23}  # Dictionary with some key-value pairs

print(marks, type(marks))  # prints the entire dictionary and its type
print(marks["Harry"])  # prints the value associated with the key "Harry"
