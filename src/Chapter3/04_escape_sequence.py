# Escape Sequences in Python
# Escape sequences are special characters that are used to represent certain whitespace or non-printable characters in a string.
# They are preceded by a backslash (\) and have a specific meaning.
# Here are some commonly used escape sequences in Python:
# \n - Newline
# \t - Tab
# \\ - Backslash
# \' - Single quote
# \" - Double quote
# \b - Backspace

a = "Hitesh \'is\' good \bgood boy\n but not a\t bad \"boy\" for \'sure\'"

print(a)